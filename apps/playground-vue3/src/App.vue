    <p>&nbsp;扎实的基础</p>
    <p>
      记得第一次面试之前，我很努力地把所知道的前端相关的技术点列了列，写了好几页纸的关键词，然后针对每个关键词系统地巩固学习，解决心中的疑问以及学习时遇到的新疑问，然后自信满满地参加了新浪和网易的面试。那时候，估计两家公司的前端工程化已经有些苗头了，但可能认为学校里的同学没啥实战经验，主要问的都是前端基础题，闭包、正则、DOM
      模型、Event
      模型等等，没什么特别深入的话题。当然，那时候我的简历中也没有太多体现工程化相关的内容，估计跟面试官也聊不起来。
    </p>
    <p>
      对于入门不久的前端同学，面试官着重考虑的是他们的学习能力、发展潜力，也会适当看看编程能力过不过关，对
      JavaScript/CSS/HTML
      的基础知识掌握的牢不牢固。如果你被面试官选中了，那就说明，你具有较强的综合素养，是一个合格的前端入门者。一般重点高校学生的学习能力都还不错，大学几门计算机相关学科的薰陶加上适当的课外练习后，编程能力也是能够接受的，所以公司喜欢招聘重点高校尤其是偏理工科的学生。我看到不少同学，在毕业前三五个月才开始了解和学习前端，单最后都顺利地进入
      BAT。
    </p>
    <p>
      我就属于上面这波人，只不过，不是毕业前抱佛脚，而是较早地接触了前端，看了很多书籍，融入了前端社区，并且在学校的技术团队中锻炼了基本功。我很庆幸自己会在大学时，投入那么多时间在前端基础知识的学习上，因为毕业后我发现，自己已经没有那么多时间和耐心来学习这些略感枯燥的知识了。已经工作的你还能够坚持一口气啃完犀牛书么？估计绝大多数人都没这个耐心吧，即便有些知识你已经淡忘了，只愿意把犀牛书作为工具书。我在毕业前就把这本书啃了四五遍，现在基本不需要这本工具书了。
    </p>
    <p>
      大学期间读过的书很多，豆瓣上记录过&nbsp;<a
