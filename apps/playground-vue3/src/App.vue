    <p>今天给大家分享的主题是前端的自我成长，这是一个关于成长的话题。</p>
    <p>
      很多人都有这样的感觉：听了很多技术圈子的分享，有的有深度，有的循循善诱，深入浅出，但是呢，几年下来，到底哪些用上了，哪些对自己真的有帮助了?反而有些模糊。
    </p>
    <p>
      2015 年我在不同的场合分享了很多内容：有移动端的性能、有适配、有 Web vs
      Native，也有
      hybrid，但是其实我一直比较担心，真正有深度的内容，其实面向的是比较小众的群体，比如说
      Hybrid，其实它在大部分公司里面，是只能用现成的。
    </p>
    <p>
      所以我这一次尝试分享一个我认为可以帮助到所有前端的话题，关于前端的成长，如果说这个分享的内容，听众里面有那么几十个人拿到
      BAT 的 offer，或者升职加薪，那么我觉得我就认为我取得了成功。
    </p>
    <p>
      前端其实是个特别苦逼的职业，因为前端技术一直革命的特别快，新技术、新技巧在不断地被发明出来。之前我有一个朋友，他讲说他对自己的认知是<strong>了解前端、熟悉前端、精通前端、熟悉前端、不懂前端</strong>。为什么呢，他说当他觉得自己对前端所有的东西觉得无所不知，无所不能的时候，忽然看到了一段代码，他完全无法理解，于是整个世界就崩塌了，从此再也不敢说自己会前端。
    </p>
    <p>
      我就跟他说，这里，缺少的是一种正确的方法，你觉得无所不知、无所不能的标准是什么，是工作中很久没遇到解决不了的问题么？他说还真是这样。我就又问他，那你系统学过前端么？他想了想，还真没学过，大学里不开这个课。的确如此，到目前为止，还没有任何一个大学会教前端，倒是有些培训班，会讲网页开发三剑客。
    </p>
    <p>我这里讲的内容，希望带给大家的，就是该如何学习前端，实现自身成长。</p>
    <p>
